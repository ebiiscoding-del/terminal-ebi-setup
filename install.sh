#!/bin/bash

# в”Җв”Җ Colors в”Җв”Җ
RED='\033[38;5;196m'
GREEN='\033[38;5;114m'
BLUE='\033[38;5;39m'
PURPLE='\033[38;5;141m'
YELLOW='\033[38;5;226m'
GRAY='\033[38;5;242m'
WHITE='\033[38;5;255m'
RESET='\033[0m'
CHECK="${GREEN}вң”${RESET}"
CROSS="${RED}вңҳ${RESET}"
ARROW="${PURPLE}вқҜ${RESET}"

# в”Җв”Җ Header в”Җв”Җ
clear
echo ""
echo -e "${PURPLE}  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв•—   в–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв•—   в–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв•—      ${RESET}"
echo -e "${BLUE}  в•ҡв•җв•җв–Ҳв–Ҳв•”в•җв•җв•қв–Ҳв–Ҳв•”в•җв•җв•җв•җв•қв–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘      ${RESET}"
echo -e "${PURPLE}     в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•”в–Ҳв–Ҳв–Ҳв–Ҳв•”в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в–Ҳв–Ҳв•— в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘      ${RESET}"
echo -e "${BLUE}     в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•”в•җв•җв•қ  в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘      ${RESET}"
echo -e "${PURPLE}     в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘ в•ҡв•җв•қ в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— ${RESET}"
echo -e "${GRAY}     в•ҡв•җв•қ   в•ҡв•җв•җв•җв•җв•җв•җв•қв•ҡв•җв•қ  в•ҡв•җв•қв•ҡв•җв•қ     в•ҡв•җв•қв•ҡв•җв•қв•ҡв•җв•қ  в•ҡв•җв•җв•җв•қв•ҡв•җв•қ  в•ҡв•җв•қв•ҡв•җв•җв•җв•җв•җв•җв•қ ${RESET}"
echo ""
echo -e "${GRAY}  Ebi's Terminal Setup вҖ” One command to rule them all${RESET}"
echo -e "${GRAY}  в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${RESET}"
echo ""

# в”Җв”Җ Check macOS в”Җв”Җ
if [[ "$OSTYPE" != "darwin"* ]]; then
    echo -e "  ${CROSS} ${RED}This script is for macOS only!${RESET}"
    exit 1
fi

echo -e "${ARROW} ${WHITE}Checking dependencies...${RESET}"
echo ""

# в”Җв”Җ Check Homebrew в”Җв”Җ
if ! command -v brew &>/dev/null; then
    echo -e "  ${ARROW} ${YELLOW}Installing Homebrew...${RESET}"
    /bin/bash -c "$(curl -fsSL https://raw.githubusercontent.com/Homebrew/install/HEAD/install.sh)"
else
    echo -e "  ${CHECK} ${GREEN}Homebrew${RESET} already installed"
fi

echo ""
echo -e "${ARROW} ${WHITE}Installing packages...${RESET}"
echo ""

# в”Җв”Җ Install packages в”Җв”Җ
PACKAGES=(
    "zsh"
    "eza"
    "zoxide"
    "fzf"
    "bat"
    "lazygit"
    "gh"
    "tldr"
    "fastfetch"
    "figlet"
    "lolcat"
    "btop"
    "git-delta"
    "onefetch"
    "ripgrep"
    "httpie"
    "zsh-autosuggestions"
    "zsh-syntax-highlighting"
    "powerlevel10k"
)

for pkg in "${PACKAGES[@]}"; do
    if brew list "$pkg" &>/dev/null; then
        echo -e "  ${CHECK} ${GREEN}$pkg${RESET} ${GRAY}already installed${RESET}"
    else
        echo -e "  ${ARROW} ${YELLOW}Installing $pkg...${RESET}"
        brew install "$pkg" &>/dev/null
        echo -e "  ${CHECK} ${GREEN}$pkg${RESET} installed"
    fi
done

echo ""
echo -e "${ARROW} ${WHITE}Installing coloreza...${RESET}"
echo ""

# в”Җв”Җ Install coloreza в”Җв”Җ
if [ ! -f ~/.coloreza.py ]; then
    git clone https://github.com/ebiiscoding-del/coloreza.git /tmp/coloreza &>/dev/null
    cp /tmp/coloreza/coloreza.py ~/.coloreza.py
    chmod +x ~/.coloreza.py
    rm -rf /tmp/coloreza
    echo -e "  ${CHECK} ${GREEN}coloreza${RESET} installed"
else
    echo -e "  ${CHECK} ${GREEN}coloreza${RESET} already installed"
fi

echo ""
echo -e "${ARROW} ${WHITE}Copying dotfiles...${RESET}"
echo ""

REPO_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# в”Җв”Җ Copy dotfiles в”Җв”Җ
DOTFILES=(
    ".zshrc"
    ".p10k.zsh"
    ".gitconfig"
)

for file in "${DOTFILES[@]}"; do
    if [ -f "$REPO_DIR/dotfiles/$file" ]; then
        cp "$REPO_DIR/dotfiles/$file" ~/$file
        echo -e "  ${CHECK} ${GREEN}$file${RESET} copied"
    else
        echo -e "  ${CROSS} ${GRAY}$file not found, skipping${RESET}"
    fi
done

echo ""
echo -e "${ARROW} ${WHITE}Setting up fastfetch...${RESET}"
echo ""

# в”Җв”Җ Fastfetch config в”Җв”Җ
mkdir -p ~/.config/fastfetch
if [ -f "$REPO_DIR/fastfetch/config.jsonc" ]; then
    cp "$REPO_DIR/fastfetch/config.jsonc" ~/.config/fastfetch/
    cp "$REPO_DIR/fastfetch/combined_logo.txt" ~/.config/fastfetch/
    cp "$REPO_DIR/fastfetch/theerv.txt" ~/.config/fastfetch/
    echo -e "  ${CHECK} ${GREEN}fastfetch${RESET} configured"
fi

echo ""
echo -e "${ARROW} ${WHITE}Installing scripts...${RESET}"
echo ""

# в”Җв”Җ Copy scripts в”Җв”Җ
SCRIPTS=(
    "organise_downloads.sh"
    "organise_screenshots.sh"
    "backup_dotfiles.sh"
    "pomodoro.sh"
)

for script in "${SCRIPTS[@]}"; do
    if [ -f "$REPO_DIR/scripts/$script" ]; then
        cp "$REPO_DIR/scripts/$script" ~/.$script
        chmod +x ~/.$script
        echo -e "  ${CHECK} ${GREEN}$script${RESET} installed"
    fi
done

echo ""
echo -e "${ARROW} ${WHITE}Setting up launchd agents...${RESET}"
echo ""

# в”Җв”Җ Launchd agents в”Җв”Җ
mkdir -p ~/Library/LaunchAgents

for plist in "$REPO_DIR"/launchd/*.plist; do
    filename=$(basename "$plist")
    cp "$plist" ~/Library/LaunchAgents/
    launchctl load ~/Library/LaunchAgents/$filename 2>/dev/null
    echo -e "  ${CHECK} ${GREEN}$filename${RESET} loaded"
done

echo ""
echo -e "${GRAY}  в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${RESET}"
echo ""
echo -e "  ${CHECK} ${GREEN}All done!${RESET} рҹҺү"
echo ""
echo -e "  ${WHITE}Run the following to apply:${RESET}"
echo ""
echo -e "  ${CYAN}source ~/.zshrc${RESET}"
echo ""
echo -e "  ${GRAY}Enjoy your new terminal! рҹҡҖ${RESET}"
echo ""
